function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

function hashString(value) {
  let hash = 0;
  for (let i = 0; i < value.length; i += 1) {
    hash = (hash * 31 + value.charCodeAt(i)) | 0;
  }
  return Math.abs(hash);
}

export function detectProductImageType(product) {
  const title = `${product.title || ""}`.toLowerCase();
  const subcategory = `${product.subcategoryName || product.subcategory?.name || ""}`.toLowerCase();
  const category = `${product.categoryName || product.category?.name || ""}`.toLowerCase();
  const brand = `${product.brand || product.brand?.name || ""}`.toLowerCase().trim();

  const isWatchByBrand = WATCH_BRANDS.has(brand);
  const isWatchByTitle = WATCH_KEYWORDS.some((keyword) => title.includes(keyword));
  const isJewelryByTitle = JEWELRY_KEYWORDS.some((keyword) => title.includes(keyword));

  if (subcategory.includes("bag") || title.includes("bag") || title.includes("tote") || title.includes("wallet")) return "bag";
  if (category.includes("shoe") || subcategory.includes("shoe") || subcategory.includes("sneaker") || subcategory.includes("boot") || subcategory.includes("sandal")) return "shoes";
  if (isWatchByBrand || isWatchByTitle) return "watch";
  if (subcategory.includes("jewel") || isJewelryByTitle) return "jewelry";
  return "clothing";
}

export function buildProductKeywords(product) {
  const type = detectProductImageType(product);
  const title = `${product.title || ""}`.toLowerCase();
  const brand = `${product.brand || product.brand?.name || ""}`.trim();
  const color = COLOR_WORDS.find((c) => title.includes(c));
  const typeKeyword =
    type === "bag" ? "bag" :
      type === "shoes" ? "shoes" :
        type === "watch" ? "wrist watch" :
          type === "jewelry" ? "jewelry" : "fashion";

  return [brand, color, typeKeyword]
    .filter(Boolean)
    .map((word) => `${word}`.trim().toLowerCase())
    .filter((word, idx, arr) => arr.indexOf(word) === idx);
}

function sanitizeKeywords(keywords) {
  return keywords
    .map((k) => `${k}`.toLowerCase().replace(/[^a-z0-9\s-]/g, "").trim())
    .filter(Boolean)
    .slice(0, 4);
}

function pickFromPool(pool, index = 0, count = 2) {
  if (!pool?.length) return [];
  if (count <= 1) return [pool[index % pool.length]];
  const first = pool[index % pool.length];
  const second = pool[(index + 1) % pool.length];
  return first === second ? [first] : [first, second];
}

export function getFallbackImages(product, index = 0, count = 2) {
  const type = detectProductImageType(product);
  const pool = POOL_BY_TYPE[type] || CLO_IMGS;
  return pickFromPool(pool, index, count);
}

export function isValidImageUrl(url) {
  if (!url || typeof url !== "string") return false;
  try {
    const parsed = new URL(url);
    return parsed.protocol === "http:" || parsed.protocol === "https:";
  } catch {
    return false;
  }
}

export async function fetchLoremFlickrImageUrl({
  keywords,
  lock,
  width = 800,
  height = 800,
  timeoutMs = 6000,
  retries = 2,
}) {
  const cleanKeywords = sanitizeKeywords(keywords);
  if (!cleanKeywords.length) return null;

  const tags = cleanKeywords.map((tag) => encodeURIComponent(tag)).join(",");
  const endpoint = `https://loremflickr.com/json/${width}/${height}/${tags}/all?lock=${lock}`;

  for (let attempt = 0; attempt <= retries; attempt += 1) {
    const controller = new AbortController();
    const timeout = setTimeout(() => controller.abort(), timeoutMs);
    try {
      const res = await fetch(endpoint, { signal: controller.signal });
      if (!res.ok) throw new Error(`HTTP ${res.status}`);
      const data = await res.json();
      if (isValidImageUrl(data?.file)) return data.file;
      throw new Error("No valid image URL");
    } catch {
      if (attempt === retries) return null;
      await sleep(250 * (attempt + 1));
    } finally {
      clearTimeout(timeout);
    }
  }
  return null;
}

export async function resolveProductImages(product, options = {}) {
  const {
    index = 0,
    count,
    delayMs = 120,
    timeoutMs = 6000,
    retries = 2,
  } = options;

  const type = detectProductImageType(product);
  const targetCount = typeof count === "number" ? count : (type === "watch" || type === "jewelry" ? 1 : 2);
  const keywords = buildProductKeywords(product);
  const seed = hashString(`${product.title || ""}-${product.brand || product.brand?.name || ""}`);

  const picked = [];
  for (let i = 0; i < targetCount; i += 1) {
    const url = await fetchLoremFlickrImageUrl({
      keywords,
      lock: seed + index + i,
      timeoutMs,
      retries,
    });
    if (isValidImageUrl(url) && !picked.includes(url)) picked.push(url);
    if (delayMs > 0) await sleep(delayMs);
  }

  if (picked.length) return picked;
  return getFallbackImages(product, index, targetCount);
}
